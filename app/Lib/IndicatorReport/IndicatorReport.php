            DISTINCT(`ut_indicator_en`.`Indicator_Nid`),
            `ut_indicator_en`.`Indicator_Name`
        FROM `ut_data`
        INNER JOIN `ut_indicator_en` ON `ut_indicator_en`.`Indicator_Nid` = `ut_data`.`Indicator_NId`;
EOD;

        $resultSet = $this->datasource['di6']->fetchAll($sql);
