    WHERE `ut_data`.`Indicator_NId` = {{ Indicator_Nid }} AND `ut_data`.`Area_Nid` IN ({{ Area_Nid }}) AND `ut_data`.`Timeperiod_Nid` IN ({{ Timeperiod_Nid }});
